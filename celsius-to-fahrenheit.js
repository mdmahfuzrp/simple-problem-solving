// Celsius to Fahrenheit
function celsToFah(celsius){

    // Formula for Celsius to Fahrenheit Convertion
    var fahrenheit = (celsius * 9/5) + 32;
    return fahrenheit;
}

const fahrenheitResult = celsToFah(20);
console.log(fahrenheitResult);

