// Problem Name: get the inch input and calculate inch to feet
function inchToFeet(inch){
    var feet = inch / 12;
    // return the result of feet to inch
    return feet;
}
// Input or inch
const inch = 40;
// Recived the result from function
const getFeet = parseFloat(inchToFeet(inch).toFixed(2));
// Output
console.log(getFeet);


